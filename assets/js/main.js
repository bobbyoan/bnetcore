function switchLanguage(lang) {
    document.documentElement.lang = lang;
    localStorage.setItem('bnetcore-lang', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang]?.[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.setAttribute('placeholder', translations[lang][key]);
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        if (translations[lang]?.[key]) {
            el.setAttribute('aria-label', translations[lang][key]);
        }
    });

    const enBtns = document.querySelectorAll('.lang-btn-en');
    const roBtns = document.querySelectorAll('.lang-btn-ro');
    if (lang === 'en') {
        enBtns.forEach(b => b.className = "lang-btn-en flex-1 px-2 py-1.5 rounded transition text-brand-cyan border border-brand-cyan/30");
        roBtns.forEach(b => b.className = "lang-btn-ro flex-1 px-2 py-1.5 rounded transition text-slate-400 border border-transparent hover:text-white");
    } else {
        roBtns.forEach(b => b.className = "lang-btn-ro flex-1 px-2 py-1.5 rounded transition text-brand-cyan border border-brand-cyan/30");
        enBtns.forEach(b => b.className = "lang-btn-en flex-1 px-2 py-1.5 rounded transition text-slate-400 border border-transparent hover:text-white");
    }
}

// Theme Controller (light / dark / system)
const systemThemeQuery = window.matchMedia('(prefers-color-scheme: dark)');

function resolveTheme(pref) {
    return pref === 'system' ? (systemThemeQuery.matches ? 'dark' : 'light') : pref;
}

function updateThemeButtons(pref) {
    document.querySelectorAll('.theme-btn').forEach(btn => {
        const isActive = btn.getAttribute('data-theme-btn') === pref;
        btn.classList.toggle('bg-brand-cyan/20', isActive);
        btn.classList.toggle('ring-1', isActive);
        btn.classList.toggle('ring-brand-cyan/50', isActive);
    });
}

function setTheme(pref) {
    localStorage.setItem('bnetcore-theme', pref);
    document.documentElement.setAttribute('data-theme', resolveTheme(pref));
    updateThemeButtons(pref);
}

systemThemeQuery.addEventListener('change', () => {
    const storedPref = localStorage.getItem('bnetcore-theme') || 'system';
    if (storedPref === 'system') {
        document.documentElement.setAttribute('data-theme', resolveTheme('system'));
    }
});

document.addEventListener('DOMContentLoaded', () => {
    const storedLang = localStorage.getItem('bnetcore-lang') || 'ro';
    switchLanguage(storedLang);

    const storedTheme = localStorage.getItem('bnetcore-theme') || 'system';
    setTheme(storedTheme);

    // Hamburger Menu Mobile Controller
    const mobileMenuBtn = document.getElementById('mobile-menu-btn');
    const mobileMenu = document.getElementById('mobile-menu');
    const menuIcon = document.getElementById('menu-icon');

    if (mobileMenuBtn && mobileMenu && menuIcon) {
        mobileMenuBtn.addEventListener('click', () => {
            const isHidden = mobileMenu.classList.toggle('hidden');
            menuIcon.classList.toggle('fa-bars', isHidden);
            menuIcon.classList.toggle('fa-xmark', !isHidden);
            closeSettingsPanels();
        });

        document.querySelectorAll('#mobile-menu a').forEach(link => {
            link.addEventListener('click', () => {
                mobileMenu.classList.add('hidden');
                menuIcon.classList.add('fa-bars');
                menuIcon.classList.remove('fa-xmark');
                closeSettingsPanels();
            });
        });
    }

    // Settings Dropdown (language + theme, grouped behind a single gear icon)
    function closeSettingsPanels() {
        document.querySelectorAll('.settings-panel').forEach(p => p.classList.add('hidden'));
        document.querySelectorAll('.settings-toggle').forEach(t => t.setAttribute('aria-expanded', 'false'));
    }

    document.querySelectorAll('[data-settings]').forEach(wrapper => {
        const toggle = wrapper.querySelector('.settings-toggle');
        const panel = wrapper.querySelector('.settings-panel');
        if (!toggle || !panel) return;

        toggle.addEventListener('click', (e) => {
            e.stopPropagation();
            const isOpen = !panel.classList.contains('hidden');
            closeSettingsPanels();
            if (!isOpen) {
                panel.classList.remove('hidden');
                toggle.setAttribute('aria-expanded', 'true');
            }
        });
    });

    document.addEventListener('click', (e) => {
        document.querySelectorAll('[data-settings]').forEach(wrapper => {
            if (!wrapper.contains(e.target)) {
                const panel = wrapper.querySelector('.settings-panel');
                const toggle = wrapper.querySelector('.settings-toggle');
                if (panel) panel.classList.add('hidden');
                if (toggle) toggle.setAttribute('aria-expanded', 'false');
            }
        });
    });

    // Fixed Offset Header Smooth Scroll (the Contact CTA below has its own handler)
    document.querySelectorAll('a[href^="#"]:not(.js-contact-cta)').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const targetID = this.getAttribute('href');
            if (targetID === '#') return;
            const targetElement = document.querySelector(targetID);
            if (targetElement) {
                e.preventDefault();
                const headerOffset = 80;
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.scrollY - headerOffset;
                window.scrollTo({ top: offsetPosition, behavior: 'smooth' });
            }
        });
    });

    // Contact CTA: smooth-scroll to the Contact section and briefly highlight it.
    // If the section is already in view, do nothing.
    document.querySelectorAll('.js-contact-cta').forEach(cta => {
        cta.addEventListener('click', (e) => {
            const target = document.getElementById('contact');
            if (!target) return;
            e.preventDefault();

            const headerOffset = 80;
            const targetPosition = target.getBoundingClientRect().top + window.scrollY - headerOffset;
            const alreadyThere = Math.abs(window.scrollY - targetPosition) < 4;
            if (alreadyThere) return;

            window.scrollTo({ top: targetPosition, behavior: 'smooth' });
            target.classList.remove('section-highlight');
            // Force reflow so re-adding the class restarts the animation on repeat clicks.
            void target.offsetWidth;
            target.classList.add('section-highlight');
            setTimeout(() => target.classList.remove('section-highlight'), 1600);
        });
    });

    // Contact info cards: click-to-call/email, keyboard accessible, with copy-to-clipboard
    async function copyText(value) {
        if (navigator.clipboard && window.isSecureContext) {
            try {
                await navigator.clipboard.writeText(value);
                return true;
            } catch (err) { /* fall through to legacy fallback */ }
        }
        try {
            const ta = document.createElement('textarea');
            ta.value = value;
            ta.style.position = 'fixed';
            ta.style.opacity = '0';
            document.body.appendChild(ta);
            ta.select();
            document.execCommand('copy');
            document.body.removeChild(ta);
            return true;
        } catch (err) {
            return false;
        }
    }

    document.querySelectorAll('[data-contact-action]').forEach(card => {
        const activate = () => { window.location.href = card.getAttribute('data-contact-action'); };
        card.addEventListener('click', activate);
        card.addEventListener('keydown', (e) => {
            // Ignore keydowns bubbling up from the nested copy button — it handles its own activation.
            if (e.target !== card) return;
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                activate();
            }
        });
    });

    document.querySelectorAll('.copy-btn').forEach(btn => {
        btn.addEventListener('click', async (e) => {
            e.stopPropagation();
            const value = btn.getAttribute('data-copy-value');
            const tooltipKey = btn.getAttribute('data-copy-tooltip-key');
            const currentLang = localStorage.getItem('bnetcore-lang') || 'ro';
            const tooltip = btn.querySelector('.copy-tooltip');

            await copyText(value);

            if (tooltip) {
                if (translations[currentLang]?.[tooltipKey]) {
                    tooltip.textContent = translations[currentLang][tooltipKey];
                }
                tooltip.classList.add('opacity-100');
                clearTimeout(btn._tooltipTimeout);
                btn._tooltipTimeout = setTimeout(() => tooltip.classList.remove('opacity-100'), 1500);
            }
        });
    });
});
